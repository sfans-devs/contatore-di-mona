#include <iostream>
using namespace std;
int main() {
    unsigned long long mona, cont;
    cout << "inserire il numero di mona: ";
    cin >> mona; cont = mona; //CDs Nuts
    while (cont <= mona) {
        cout << "mona";
        cont++;
    }
    cout << "conteggio terminato: risultati";
    cout << (mona + " mona, " + cont + " iterazioni");
    return 0;
}
